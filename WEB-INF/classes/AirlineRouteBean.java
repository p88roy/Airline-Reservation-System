public class AirlineRouteBean
{
 private String routeCode;
 private String flightFrom;
 private String flightTo;
 private String frequency;

 public AirlineRouteBean() {
 }
 
 public void setRouteCode(String aRouteCode) {
   routeCode= aRouteCode;
}
 public String getRouteCode() {
   return routeCode;
}


public void setFlightFrom(String aFlightFrom) {
   flightFrom= aFlightFrom;
}
 public String getFlightFrom() {
   return flightFrom;
}


public void setFlightTo(String aFlightTo) {
   flightTo= aFlightTo;
}
 public String getFlightTo() {
   return flightTo;
}


public void setFrequency(String aFrequency) {
   frequency= aFrequency;
}
 public String getFrequency() {
   return frequency;
}

}
